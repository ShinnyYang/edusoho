<template>
  <div class="list-item" @click="goToWrongQuestionDetail">
    <div class="list-item__image">
      <img :src="question.target_data.cover.middle" />
    </div>
    <div class="list-item__detail">
      <h3 class="title line-clamp-2">{{ question.target_data.title }}</h3>
      <p class="number text-overflow">{{ question.item_num }} {{ $t('wrongQuestion.question') }}</p>
    </div>
  </div>
</template>

<script>
export default {
  props: {
    question: {
      type: Object,
      required: true,
    },
  },

  methods: {
    goToWrongQuestionDetail() {
      const { target_type } = this.question;
      let routerName = 'myWrongQuestionBookDetail';
      if (target_type === 'exercise') {
        routerName = 'myWrongQuestionBookExercise';
      }
      this.$router.push({
        name: routerName,
        params: {
          type: target_type,
          id: this.question.id,
        },
        query: {
          title: this.question.target_data.title,
          id: this.question.id,
        },
      });
    },
  },
};
</script>
