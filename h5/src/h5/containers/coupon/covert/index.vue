<template>
  <div class="coupon-covert-container">
    <h2 class="title">{{ $t('title.coupon') }}</h2>
    <van-field
      v-model.trim="code"
      :class="[{ 'error-code': isErrorCode }, 'e-input', 'coupon-input']"
      :placeholder="$t('placeholder.voucherCode')"
      clearable
      @input="checkCodeChange(code)"
    />
    <span v-if="isErrorCode" class="error-code text-14">{{ errorText }}</span>
    <van-button
      :disabled="btnDisable || isErrorCode"
      type="info"
      class="covert-submit"
      @click="codeCovert(code)"
      >{{ $t('btn.confirm') }}</van-button
    >
    <van-popup
      v-model="popupShow"
      :close-on-click-overlay="closeOnClickOverlay"
      class="coupon-covert-popup"
    >
      <div class="modal-content">
        兑换成功
        <div>恭喜您获得{{ courseTitle }}课程</div>
      </div>
      <div class="color-primary mt10 text-15" @click="toStudy">去学习</div>
    </van-popup>
  </div>
</template>
<script>
import Api from '@/api';

export default {
  data() {
    return {
      code: '',
      popupShow: false,
      closeOnClickOverlay: false,
      btnDisable: true,
      isErrorCode: false,
      errorText: '',
      courseId: '',
      courseTitle: '',
    };
  },

  watch: {
    code(val, oldVal) {
      const lengthDiff = val.length - oldVal.length;
      // 粘贴内容的自动识别填充
      if (Math.abs(lengthDiff) > 8) {
        const spliceCode = this.spliceCode(val);
        this.code = spliceCode || val;
        this.isErrorCode = false;
        this.btnDisable = false;
        this.errorText = '';
      }
    },
  },

  methods: {
    spliceCode(code) {
      const reg = /[a-z0-9A-Z]{8}/;
      return code.match(reg) ? code.match(reg)[0] : false;
    },
    checkCodeChange(code) {
      if (code.length > 7) {
        const reg = /^[a-z0-9A-Z]{8}$/;
        const correctCode = reg.test(code);
        if (!correctCode) {
          this.isErrorCode = true;
          this.errorText = '8位数字、英文字母组成';
          return;
        }
        this.isErrorCode = false;
        this.btnDisable = false;
        this.errorText = '';
        return;
      }
      this.isErrorCode = false;
      this.btnDisable = true;
      this.errorText = '';
    },
    codeCovert(code) {
      Api.couponCheck({
        query: {
          code: code,
        },
      }).then(res => {
        console.log(res, 'couponCheck res');
        if (res.success === false || res.error) {
          this.isErrorCode = true;
          this.errorText = res.error.message;
          return;
        }
        Api.exchangeCoupon({
          query: {
            code: code,
          },
        })
          .then(res => {
            console.log('exchangeCoupon', res);
            this.courseId = res.products[0].course.id;
            this.courseTitle = res.products[0].course.displayedTitle;
            this.popupShow = true;
          })
          .catch(error => {
            this.isErrorCode = true;
            this.errorText = error.message;
          });
      });
    },
    toStudy() {
      const courseId = this.courseId;
      if (courseId) {
        // 跳转详情页后可直接返回到我的页面
        const myUrl = encodeURIComponent('/my/orders');
        this.$router.push({
          path: `/course/${courseId}?backUrl=${myUrl}`,
        });
      }
    },
  },
};
</script>
